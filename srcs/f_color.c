#include "fractol.h"

void		f_color(t_fractol *m, int x, int y, int n)
{
	int		i;

	i = (x * 4) + (y * m->size);
	//m->data[i] = ft_rgb(255, 133, 170);
	m->data[++i] = n * m->r;
	m->data[++i] = ft_rgb(m->r, m->g, m->b);
}
